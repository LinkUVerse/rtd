#[derive(Debug, Deserialize, Serialize, JsonSchema, Clone, Eq, PartialEq)]
#[serde(rename = "MoveObject", rename_all = "camelCase")]
pub struct RtdParsedMoveObject {
    #[serde(rename = "type")]
    #[serde_as(as = "RtdStructTag")]
    #[schemars(with = "String")]
    pub type_: StructTag,
    pub has_public_transfer: bool,
    pub fields: RtdMoveStruct,
}

impl RtdMoveObject for RtdParsedMoveObject {
    fn try_from_layout(
        object: MoveObject,
        layout: MoveStructLayout,
    ) -> Result<Self, anyhow::Error> {
        let move_struct = object.to_move_struct(&layout)?.into();

        Ok(
            if let RtdMoveStruct::WithTypes { type_, fields } = move_struct {
                RtdParsedMoveObject {
                    type_,
                    has_public_transfer: object.has_public_transfer(),
                    fields: RtdMoveStruct::WithFields(fields),
                }
            } else {
                RtdParsedMoveObject {
                    type_: object.type_().clone().into(),
                    has_public_transfer: object.has_public_transfer(),
                    fields: move_struct,
                }
            },
        )
    }

    fn type_(&self) -> &StructTag {
        &self.type_
    }
}

impl RtdParsedMoveObject {
    pub fn try_from_object_read(object_read: ObjectRead) -> Result<Self, anyhow::Error> {
        let parsed_data = RtdParsedData::try_from_object_read(object_read)?;
        match parsed_data {
            RtdParsedData::MoveObject(o) => Ok(o),
            RtdParsedData::Package(_) => Err(anyhow::anyhow!("Object is not a Move object")),
        }
    }
}

pub fn type_and_fields_from_move_event_data(
    event_data: MoveValue,
) -> RtdResult<(StructTag, serde_json::Value)> {
    match event_data.into() {
        RtdMoveValue::Struct(move_struct) => match &move_struct {
            RtdMoveStruct::WithTypes { type_, .. } => {
                Ok((type_.clone(), move_struct.clone().to_json_value()))
            }
            _ => Err(RtdErrorKind::ObjectDeserializationError {
                error: "Found non-type RtdMoveStruct in MoveValue event".to_string(),
            }
            .into()),
        },
        RtdMoveValue::Variant(v) => Ok((v.type_.clone(), v.clone().to_json_value())),
        RtdMoveValue::Vector(_)
        | RtdMoveValue::Number(_)
        | RtdMoveValue::Bool(_)
        | RtdMoveValue::Address(_)
        | RtdMoveValue::String(_)
        | RtdMoveValue::UID { .. }
        | RtdMoveValue::Option(_) => Err(RtdErrorKind::ObjectDeserializationError {
            error: "Invalid MoveValue event type -- this should not be possible".to_string(),
        }
        .into()),
    }
}

